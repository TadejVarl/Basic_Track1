enter_digit = int(input("Enter a number from 1 to 7..."))

if enter_digit == 1:
    print("Monday")
elif enter_digit == 2:
    print("Tuesday")
elif enter_digit == 3:
    print ("Wednesday")
elif enter_digit == 4:
    print ("Thursday")
elif enter_digit == 5:
    print ("Friday")
elif enter_digit == 6:
    print ("Saturday")
elif enter_digit == 7:
    print ("Sunday")


